def onKeyEvent(key,isPressed):
    #-------------------------------------------------------------------
    if key == arcade.key.SPACE:
        if isPressed :
            print("SPACE KEY has been pressed !")
        else:
            print("SPACE KEY has been released !")
    if key == arcade.key.LEFT:
        var["moveL"] = isPressed
    if key == arcade.key.RIGHT:
        var["moveR"] = isPressed

